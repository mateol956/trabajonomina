    private void btnVacunasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVacunasActionPerformed
        int cantidad = Integer.parseInt(txtCantidad.getText());
        vacunas.add(new Vacunas(cantidad,"null" ,"null"));
        DefaultTableModel model = (DefaultTableModel) jTableVacunas.getModel();
        Object[] row = {vacunas.get(varContador).getAtrCantidadVacunas()};
        model.addRow(row);
        varContador++;
    }//GEN-LAST:event_btnVacunasActionPerformed

