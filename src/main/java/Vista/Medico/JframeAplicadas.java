    private void btnVacunasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVacunasActionPerformed
        String rangoEdad = txtEdad.getText();
        int cantidad = Integer.parseInt(txtVacunas.getText());
        vacunasAplicadas.add(new VacunasAplicadas(cantidad,rangoEdad));
        DefaultTableModel model = (DefaultTableModel) jTableVacunas.getModel();
        Object[] row = {vacunasAplicadas.get(varContador).getCantidad(),vacunasAplicadas.get(varContador).getRangoEdad()};
        model.addRow(row);
        varContador++;
    }//GEN-LAST:event_btnVacunasActionPerformed

