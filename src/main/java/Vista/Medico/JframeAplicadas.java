    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTableVacunas.getModel();
        for (int i = 0; i < jTableVacunas.getRowCount(); i++) {
            model.removeRow(i);
            i -= 1;
        }
        for (int varCon = 0; varCon < vacunasAplicadas.size(); varCon++) {
            Object[] row = {vacunasAplicadas.get(varContador).getCantidad(),vacunasAplicadas.get(varContador).getRangoEdad()};
            model.addRow(row);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

